package com.learnJava.JavaCrashCourse.loopingStatement;

public class WhileLoopExample {

    public static void main(String [] args){
//        While Loop - Entry Controlled Loop
        int i=0; // Initialization declared here
        while (i<=10){ //Condition declared here
            System.out.println("Learning on While Loop condition!");
            i++;  //Increment/Decrement declared here
        }
    }
}
