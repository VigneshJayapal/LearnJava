package com.learnJava.JavaCrashCourse.loopingStatementEx5;

public class DoWhileLoopEx {

    public static void main(String [] args){
//        Do While Loop - Exit Controlled Loop
        int i=0;  //Initialization declared here
        do{  //It allows without any condition to execute the block of code
            System.out.println("Learning on Do While loop condition!");
            i++;  //Increment/Decrement declared here
        } while(i<=25);  //Conditions declared here - which let the loop to run until the condition satisfied
    }
}
